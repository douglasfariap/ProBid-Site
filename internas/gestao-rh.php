                       <strong>Diagnóstico e Mapeamento de Processos de Gestão de RH</strong>

                        <br />
                        <br />

                     	Órgão de Governo de um dos principais Estados do Brasil 
                        tinha como objetivo a modernização da gestão de recursos humanos, através da 
                        revisão dos procedimentos administrativos adotados pelas unidades 
                        central e setoriais de RH.
                        
                        <br />
                        <br />

                        A adoção de procedimentos racionalizados para a folha de pagamentos e gestão 
                        de recursos humanos foi considerada prioritária para atender a estratégia 
                        de governo para modernização da gestão pública, através do mapeamento de 
                        processos e da adoção de modelo de gestão, criando as bases para a 
                        implantação de sistema informatizado de recursos humanos, com foco no 
                        desempenho e qualidade das ações das unidades de RH do Estado.
 
                        <br />
                        <br />

						O trabalho está sendo realizado paralelamente à implantação do novo sistema 
                        de gestão de recursos humanos e folha de pagamentos. Considerando o porte, 
                        complexidade, importância e riscos envolvidos, a <strong>ProBid</strong> está utilizando uma 
                        abordagem de projeto que amplia o modelo tradicional adotado para o mapeamento 
                        de processos. Através da conjugação de suas metodologias de planejamento 
                        estratégico, consultoria de gestão e BPM <em>(Business Process Management)</em>, 
                        os diferentes aspectos da organização, incluindo a variedade de procedimentos 
                        administrativos, defasagens culturais e requisitos de gestão, estão sendo 
                        trabalhados de maneira planejada e racional.
 
                        <br />
                        <br />

						Para atingir estes propósitos, esta abordagem foi organizada para tratar 
                        metodicamente todos os aspectos operacionais e institucionais relacionados 
                        com o projeto. As ações abrangem: 
 
                        <br />
                        <br />
                        
						&bull; Plano Estratégico de Projeto<br />
						&bull; Proposição de um "Modelo de Gestão de RH"<br />
						&bull; Mapeamento e Adequação de Processos<br />
						&bull; Definição de Mecanismos de Gestão<br />
						&bull; Gestão da Mudança<br />

