                        <strong>Identidade Funcional de Servidores Públicos</strong>

                        <br />
                        <br />

                        Secretaria de Estado, responsável pela gestão de pessoas, tinha como prioridades 
                        a modernização do relacionamento institucional com os servidores públicos, o 
                        aumento da segurança nas atividades relacionadas à folha de pagamentos e a qualidade 
                        da gestão de recursos humanos. Informações cadastrais de baixa confiabilidade, 
                        associadas à práticas administrativas defasadas, resultavam na deterioração das 
                        relações do Estado com seus servidores e em riscos financeiros de operações 
                        relacionadas à folha de pagamentos.
						
                    <br />
                        <br />


						O projeto foi executado sob a forma de um serviço integrado de consultoria, 
                        gerência de projeto, <em>change management</em>, operacionalização e alocação de equipes, 
                        incluindo o uso de tecnologias como identificação biométrica por impressões digitais                      
                        (AFIS – <em>Automated Fingerprint Identification System</em>) e identificação pessoal por 
                        <em>smart card</em> (cartão inteligente) de leitura automática por proximidade. 

                        <br />
                        <br />
                        
						Foram realizadas ações integradas e articuladas com o cliente para: 
   						
                        <br />
                        <br />
                        
                        &bull; Estabelecimento de um Plano Estratégico de Projeto<br />
                        &bull; Orientação de ações de comunicação institucional<br />
                        &bull; Atualização das informações funcionais e pessoais<br />
                        &bull; Planejamento logístico<br />
                        &bull; Identificação biométrica<br />
                        &bull; Operacionalização de central de tratamento de transações biométricas (<em>BSP – Biometric Service Provider</em>)<br />
                        &bull; Emissão e distribuição da Carteira de Identidade Funcional<br />
                        &bull; Implementação de novos procedimentos de segurança<br />
                        &bull; Integração com sistemas legados do Estado<br />
  					  
