                       <strong>Diagnostico Estratégico de TI e Adequação ao Modelo de Negócios</strong>

                        <br />
                        <br />

                	Instituição pública federal enfrentava situação bastante comum às organizações atuais: 
                    defasagem dos resultados da Tecnologia da Informação (TI) em relação às necessidades de negócio. 
                    Apesar da TI ser considerada ferramenta fundamental para atingir os objetivos institucionais, 
                    inadequações importantes afetavam o desempenho da organização, comprometendo a consolidação 
                    de seu papel institucional. Fatores como aplicativos obsoletos ou inadequados, inconsistências 
                    das bases de dados, ferramentas tecnológicas departamentais não integradas e insuficiência de 
                    informações estratégicas resultavam na insatisfação dos usuários dos serviços de TI.
 
                        <br />
                        <br />
                        
					Uma vez analisada a situação do Modelo de Negócio da organização, foram realizados estudos 
                    aprofundados das ferramentas, sistemas, infra-estrutura, organização e práticas de Tecnologia
                     da Informação. Toda a operação da área de TI foi avaliada e confrontada com os fatores de 
                     inadequação ao Modelo de Negócio, para identificação das causas e raízes dos problemas existentes.
                     
                        <br />
                        <br />
                        
					Estas análises subsidiaram a construção de diferentes cenários para os modelos de negócio e de TI, 
                    especificamente definidos para alavancar os resultados institucionais. A seleção do cenário mais 
                    adequado propiciou a elaboração de um plano de ações objetivo e direcionado a metas de negócios 
                    factíveis, pactuadas por toda a organização.

						
